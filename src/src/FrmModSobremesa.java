    private void btnElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirActionPerformed
        if (rbtNombre.isSelected()) {
            txtNombre.setEnabled(true);
        } else if (rbtGeneracion.isSelected()) {
            txtGeneracion.setEnabled(true);
        } else if (rbtPrecio.isSelected()) {
            txtPrecio.setEnabled(true);
        } else if (rbtDiscoDuro.isSelected()) {
            txtDiscoDuro.setEnabled(true);
        } else {
            txtVideo.setEnabled(true);
        }
    }//GEN-LAST:event_btnElegirActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Consola c = new ConsolaDeSobremesa(Integer.parseInt(txtDiscoDuro.getText()), txtVideo.getText(), clave,
                txtNombre.getText(), Integer.parseInt(txtGeneracion.getText()), Float.parseFloat(txtPrecio.getText()));
        ac.modificar(c, celda);
        JOptionPane.showConfirmDialog(this,
                "La cosola de sobremesa fue modificada",
                "Modificacion exitosa",
                JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Consola c = ac.getConsola(celda);
        txtClave.setText(String.valueOf(clave));
        String nombre = c.getNombre();
        int generacion = c.getGeneracion();
        int discoDuro = ((ConsolaDeSobremesa)c).getDiscoDuro();
        String salidaDeVideo = ((ConsolaDeSobremesa)c).getSalidaDeVideo();
        float precio = ((ConsolaDeSobremesa)c).calcularPrecioFinal();
        
        txtNombre.setText(nombre);
        txtGeneracion.setText(String.valueOf(generacion));
        txtDiscoDuro.setText(String.valueOf(discoDuro));
        txtVideo.setText(salidaDeVideo);
        txtPrecio.setText(String.valueOf(precio));
        
    }//GEN-LAST:event_formWindowOpened


